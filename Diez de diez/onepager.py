# üì¶ Cargar modelo
@st.cache_resource
def load_model():
    return joblib.load('modelo/decision_tree_pipeline.joblib')

model = load_model()

# üìÅ Cargar test set
@st.cache_data
def load_test_data():
    df = pd.read_csv('test_set.csv')
    y = df['labelExito']
    X = df.drop(columns='labelExito')
    return X, y

X_test, y_test = load_test_data()

# üìä Evaluaci√≥n
st.header("üìä Evaluaci√≥n del Modelo")

umbral = st.slider("Ajustar umbral de decisi√≥n", 0.0, 1.0, 0.3, 0.01)
probs = model.predict_proba(X_test)[:, 1]
y_pred = (probs > umbral).astype(int)

report = classification_report(y_test, y_pred, output_dict=True)
df_report = pd.DataFrame(report).transpose()

st.subheader("Reporte de Clasificaci√≥n")
st.dataframe(df_report.style.format("{:.2f}"))

# üéØ Matriz de confusi√≥n
st.subheader("Matriz de Confusi√≥n")
cm = confusion_matrix(y_test, y_pred)
tn, fp, fn, tp = cm.ravel()
st.write(f"*Falsos Positivos (FP): {fp}  |  **Falsos Negativos (FN)*: {fn}")
fig, ax = plt.subplots()
sns.heatmap(cm, annot=True, fmt='d', cmap='Blues',
            xticklabels=['No √©xito','√âxito'],
            yticklabels=['No √©xito','√âxito'],
            ax=ax)
ax.set_xlabel("Predicci√≥n")
ax.set_ylabel("Real")
st.pyplot(fig)

